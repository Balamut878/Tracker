    private func setupConstraints() {
        // safeArea - –≤–µ—Ä—Ö/–Ω–∏–∑ —ç–∫—Ä–∞–Ω–∞ —Å —É—á—ë—Ç–æ–º –≤—ã—Ä–µ–∑–∞ –∏ —Ç.–ø.
